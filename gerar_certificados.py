import datetime
from cryptography.hazmat.primitives import serialization, hashes
from cryptography.hazmat.primitives.asymmetric import rsa
from cryptography import x509
from cryptography.x509.oid import NameOID
import os

def gerar_credenciais():
    if not os.path.exists('certs'):
        os.makedirs('certs')

    # 1. Gerar Chave Privada RSA
    chave_privada = rsa.generate_private_key(
        public_exponent=65537,
        key_size=2048,
    )

    # Salvar chave privada em certs/server.key
    with open("certs/server.key", "wb") as f:
        f.write(chave_privada.private_bytes(
            encoding=serialization.Encoding.PEM,
            format=serialization.PrivateFormat.TraditionalOpenSSL,
            encryption_algorithm=serialization.NoEncryption(),
        ))

    # 2. Gerar Certificado Autoassinado (server.crt)
    subject = issuer = x509.Name([
        x509.NameAttribute(NameOID.COMMON_NAME, u"MeuServidorSeguro"),
    ])
    
    cert = x509.CertificateBuilder().subject_name(
        subject
    ).issuer_name(
        issuer
    ).public_key(
        chave_privada.public_key()
    ).serial_number(
        x509.random_serial_number()
    ).not_valid_before(
        datetime.datetime.utcnow()
    ).not_valid_after(
        datetime.datetime.utcnow() + datetime.timedelta(days=365)
    ).sign(chave_privada, hashes.SHA256())

    # Salvar certificado em certs/server.crt
    with open("certs/server.crt", "wb") as f:
        f.write(cert.public_bytes(serialization.Encoding.PEM))

    print("âœ… Sucesso: certs/server.key e certs/server.crt criados!")

if __name__ == "__main__":
    gerar_credenciais()